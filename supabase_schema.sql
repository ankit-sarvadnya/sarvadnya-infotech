-- SQL Schema for site_settings table in Supabase
-- This table is used to store dynamic configuration and contact information for the Sarvadnya Infotech website.

-- 1. Create the table
CREATE TABLE IF NOT EXISTS public.site_settings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    key TEXT UNIQUE NOT NULL,
    value TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. Set up Row Level Security (RLS)
-- Allow anyone to read the settings (anonymous access)
ALTER TABLE public.site_settings ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Allow public read access" ON public.site_settings
    FOR SELECT USING (true);

