window.PI = Math.PI;
window.cos = Math.cos;
window.sin = Math.sin;
window.abs = Math.abs;
window.sqrt = Math.sqrt;
window.pow = Math.pow;
window.round = Math.round;
window.random = Math.random;
window.atan2 = Math.atan2;
window.HALF_PI = 0.5 * window.PI;
window.TAU = 2 * window.PI;
window.TO_RAD = window.PI / 180;
window.floor = n => n | 0;
window.rand = n => n * window.random();
window.randIn = (min, max) => window.rand(max - min) + min;
window.randRange = n => n - window.rand(2 * n);
window.fadeIn = (t, m) => t / m;
window.fadeOut = (t, m) => (m - t) / m;
window.fadeInOut = (t, m) => {
	let hm = 0.5 * m;
	return window.abs((t + hm) % m - hm) / (hm);
};
window.dist = (x1, y1, x2, y2) => window.sqrt(window.pow(x2 - x1, 2) + window.pow(y2 - y1, 2));
window.angle = (x1, y1, x2, y2) => window.atan2(y2 - y1, x2 - x1);
window.lerp = (n1, n2, speed) => (1 - speed) * n1 + speed * n2;
